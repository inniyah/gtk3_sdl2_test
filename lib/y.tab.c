sx_script_id
sx_load_file (SX_SYSTEM *system, char *file) {
	int ret, flags;
	SX_VALUE *sfile;

	if (file == NULL) {
		sxin = stdin;
	} else {
		sxin = fopen (file, "r");
		if (sxin == NULL) {
			fprintf (stderr, "Could not open '%s'\n", file);
			return 0;
		}
	}

	parse_system = system;
	parse_block = sx_new_block (system);
	if (parse_block == NULL) {
		if (sxin != NULL) {
			fclose (sxin);
		}
		return 0;
	}

	flags = system->flags;
	system->flags |= SX_SFLAG_GCOFF;

	parser_push (parse_block);
	sfile = sx_new_str (system, file ? file : "<stdin>");
	pushv (sfile);
	pushv (sx_new_num (1));
	pushn (SX_OP_SETFILELINE);
	ret = sxparse ();

	parser_pop ();
	system->flags = flags;

	if (file != NULL) {
		fclose (sxin);
	}

	sx_lock_value (parse_block);

	cleanup_parser ();
	sx_run_gc (system);

	sx_unlock_value (parse_block);

	if (ret) {
		return 0;
	}

	return sx_new_script (system, NULL, file, parse_block);
}

